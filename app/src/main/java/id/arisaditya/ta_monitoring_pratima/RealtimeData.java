package id.arisaditya.ta_monitoring_pratima;

public class RealtimeData {
    private String Api;
    private String JarakPratima;
    private String Kelembaban;
    private String Pergerakan;
    private String Suhu;

    public String getApi() {
        return Api;
    }

    public void setApi(String api) {
        Api = api;
    }

    public String getJarakPratima() {
        return JarakPratima;
    }

    public void setJarakPratima(String jarakPratima) {
        JarakPratima = jarakPratima;
    }

    public String getKelembaban() {
        return Kelembaban;
    }

    public void setKelembaban(String kelembaban) {
        Kelembaban = kelembaban;
    }

    public String getPergerakan() {
        return Pergerakan;
    }

    public void setPergerakan(String pergerakan) {
        Pergerakan = pergerakan;
    }

    public String getSuhu() {
        return Suhu;
    }

    public void setSuhu(String suhu) {
        Suhu = suhu;
    }
}
